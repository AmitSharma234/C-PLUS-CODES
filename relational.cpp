// CPP Program to demonstrate the Relational Operators
#include <iostream>
using namespace std;

int main()
{
	int a = 6, b = 4;

	cout << "a == b is " << (a == b) << endl;  // Equal to 
 
	cout << "a > b is " << (a > b) << endl;  // Greater than 

	// Greater than or Equal to 
	cout << "a >= b is " << (a >= b) << endl;

	// Lesser than 
	cout << "a < b is " << (a < b) << endl;

	// Lesser than or Equal to 
	cout << "a <= b is " << (a <= b) << endl;

	// Not Equal to
	cout << "a != b is " << (a != b) << endl;

	return 0;
}
