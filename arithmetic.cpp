// CPP Program to demonstrate the Binary Operators
#include <iostream>
using namespace std;

int main()
{
	int a = 8, b = 3;
    
	cout << "a + b = " << (a + b) << endl;  // Addition
	
	cout << "a - b = " << (a - b) << endl;  // Subtraction 
	
	cout << "a * b = " << (a * b) << endl;  // Multiplication
	
	cout << "a / b = " << (a / b) << endl;  // Division 

	cout << "a % b = " << (a % b) << endl;  // Modulo 

	return 0;
}
