// CPP Program to demonstrate the Logical Operators
#include <iostream>
using namespace std;

int main()
{
	int a = 6, b = 4;

	cout << "a && b is " << (a && b) << endl;   // Logical AND

	cout << "a ! b is " << (a > b) << endl;   // Logical OR 

	cout << "!b is " << (!b) << endl;   // Logical NOT

	return 0;
}
